message="  hello World. My name is Seyma"
print(message.upper()) #dizinin tüm elemanlarını büyük yapar
print(message.lower()) #dizinin tüm elemanlarını küçük yapar
print(message.title()) #dizinin tüm elemanlarını büyük harfle başlar yapar
print(message.capitalize()) #dizinin sadece ilk elemanını büyük yapar
print(message.strip()) #dizinin başında boşluk varsa siler
print(message.split()) #dizinin elemanları boşlıklarından ayrılır,başak bir dizi (liste) oluşturur
print(message.split(".")) #dizinin elemanları noktalarından ayrılır,başak bir dizi oluşturur
print(message.join("*")) #dizinin elemanları boşlıklarından ayrılırmıştı şimdi tekrar birleşti
